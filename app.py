def build_funnel_chart(df: pd.DataFrame, lang: Dict[str, str]) -> Optional[Figure]:
    if df.empty:
        return None

    # PySpark output uses: sessions_with_view, sessions_with_add_to_cart, sessions_with_purchase
    view_col = pick_first_existing_column(df, [
        "view_count",
        "views",
        "view",
        "sessions_with_view",
        "sessions_with_views",
    ])
    cart_col = pick_first_existing_column(df, [
        "cart_count",
        "cart",
        "add_to_cart_count",
        "add_to_cart",
        "sessions_with_add_to_cart",
    ])
    purchase_col = pick_first_existing_column(df, [
        "purchase_count",
        "purchases",
        "purchase",
        "sessions_with_purchase",
    ])

    # PySpark pipeline writes: sessions_with_view, sessions_with_add_to_cart, sessions_with_purchase
    # We still keep the debugging contract, but we show missing fields for BOTH naming schemes.
    expected_spark = ["sessions_with_view", "sessions_with_add_to_cart", "sessions_with_purchase"]
    expected_alt = ["view_count", "cart_count", "purchase_count"]
    # For funnel, we should not block rendering if the alternate schema is missing.
    # We'll render only when at least one of the supported column naming schemes is available.
    supported_funnel_targets = expected_spark + expected_alt

    # First, check the PySpark-written schema (this should usually match).
    debug_schema_if_missing(
        df,
        expected=expected_spark,
        title=T["columns_debug_title"],
        error_title=T["error_expected_columns_missing"],
    )

    # Then, check alternative naming (only for visibility).
    debug_schema_if_missing(
        df,
        expected=expected_alt,
        title=T["columns_debug_title"],
        error_title=T["error_expected_columns_missing"],
    )

    # Render if we can find at least one supported naming scheme.
    # funnel requires all three stages, so we validate whichever naming scheme matched.
    if not ((view_col and cart_col and purchase_col)): 
        # Show a minimal preview to help user validate schema.
        if not df.empty:
            st.write("Funnel schema preview:")
            st.write(df.head(1))
        return None

    funnel_df = pd.DataFrame(
        [
            {"stage": lang["funnel_view"], "count": to_numeric_sum(df[view_col])},
            {"stage": lang["funnel_cart"], "count": to_numeric_sum(df[cart_col])},
            {"stage": lang["funnel_purchase"], "count": to_numeric_sum(df[purchase_col])},
        ]
    )

    fig = px.funnel(
        funnel_df,
        x="count",
        y="stage",
        title=lang["funnel_title"],
        orientation="h",
        color="stage",
        color_discrete_sequence=COLOR_SEQ,
    )

    fig.update_layout(
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        font=dict(color="#E8E8E8"),
        title_font=dict(color="#E8E8E8"),
        margin=dict(l=20, r=20, t=60, b=20),
        showlegend=False,
    )
    fig.update_xaxes(showgrid=False, tickfont=dict(color="#E8E8E8"))
    fig.update_yaxes(showgrid=False, tickfont=dict(color="#E8E8E8"))

    return fig


def build_trending_bar_chart(df: pd.DataFrame, lang: Dict[str, str]) -> Tuple[Optional[Figure], pd.DataFrame]:
    if df.empty:
        return None, pd.DataFrame()

    product_col = pick_first_existing_column(df, ["product_id", "product", "productId"])
    category_col = pick_first_existing_column(df, ["category", "cat"])
    count_col = pick_first_existing_column(df, ["count", "purchase_count", "views", "metric_count"])

    debug_schema_if_missing(
        df,
        expected=["product_id", "category", "count"],
        title=T["columns_debug_title"],
        error_title=T["error_expected_columns_missing"],
    )

    if not (product_col and count_col):
        return None, df

    out = df.copy()

    if category_col and category_col in out.columns:
        out["product_label"] = out.apply(
            lambda r: f"{r.get(category_col, '')} • {r.get(product_col, '')}".strip(" •"), axis=1
        )
    else:
        out["product_label"] = out[product_col].astype(str)

    out["count_numeric"] = pd.to_numeric(out[count_col], errors="coerce").fillna(0)

    # Required: explicitly restrict to 10 and sort (highest at top)
    out = out.sort_values("count_numeric", ascending=False).nlargest(10, "count_numeric")

    fig = px.bar(
        out,
        x="count_numeric",
        y="product_label",
        orientation="h",
        title=lang["trending_title"],
        color="count_numeric",
        color_continuous_scale=["#0f4c75", "#3282b8", "#bbe1fa"],
    )

    fig.update_layout(
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(0,0,0,0)",
        font=dict(color="#E8E8E8"),
        title_font=dict(color="#E8E8E8"),
        margin=dict(l=20, r=20, t=60, b=20),
        showlegend=False,
        xaxis_title=lang["trending_x"],
        yaxis_title=lang["trending_y"],
    )

    fig.update_xaxes(showgrid=False, tickfont=dict(color="#E8E8E8"), title_font=dict(color="#E8E8E8"))
    fig.update_yaxes(showgrid=False, tickfont=dict(color="#E8E8E8"))

    # Table - top 10
    table_df = out.copy()
    # Normalize names
    if product_col != "product_id" and product_col in table_df.columns:
        table_df = table_df.rename(columns={product_col: "product_id"})
    if category_col and category_col in table_df.columns and category_col != "category":
        table_df = table_df.rename(columns={category_col: "category"})
    if count_col != "count" and count_col in table_df.columns:
        table_df = table_df.rename(columns={count_col: "count"})

    table_df["count"] = table_df.get("count", table_df["count_numeric"]).fillna(0)
    table_df = table_df[[c for c in ["product_id", "category", "count", "product_label"] if c in table_df.columns]]
    table_df = table_df.rename(columns={"product_label": "product"})

    return fig, table_df.reset_index(drop=True)


