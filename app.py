@st.cache_resource(show_spinner=False)
def get_mongo_client(uri: str = "mongodb://localhost:27017/") -> MongoClient:
    """Cached MongoDB client to avoid creating thousands of connections."""

    return MongoClient(uri, serverSelectionTimeoutMS=3000)


def mongo_ping_ok(client: MongoClient) -> bool:
    try:
        client.admin.command("ping")
        return True
    except Exception:
        return False


def safe_docs_to_df(docs: List[Dict[str, Any]]) -> pd.DataFrame:
    if not docs:
        return pd.DataFrame()
    for d in docs:
        d.pop("_id", None)
    return pd.DataFrame(docs)


def pick_first_existing_column(df: pd.DataFrame, candidates: List[str]) -> Optional[str]:
    for c in candidates:
        if c in df.columns:
            return c
    return None


def to_numeric_sum(series: Optional[pd.Series]) -> float:
    if series is None or len(series) == 0:
        return 0.0
    return float(pd.to_numeric(series, errors="coerce").fillna(0).sum())


@st.cache_data(show_spinner=False)
def load_collection_as_df(db_name: str, collection_name: str, limit: int = 10000) -> pd.DataFrame:
    client = get_mongo_client("mongodb://localhost:27017/")
    coll = client[db_name][collection_name]
    docs = list(coll.find({}, limit=limit))
    return safe_docs_to_df(docs)


