def debug_schema_if_missing(df: pd.DataFrame, expected: List[str], title: str, error_title: str) -> None:
    """Show missing-column diagnostics + required column preview.

    The dashboard should remain stable (never crash) even if expected columns differ.
    """

    if df.empty:
        return

    missing = [c for c in expected if c not in df.columns]
    if not missing:
        return

    # Required by user: show df.head(1) so they can see actual Mongo/PySpark output columns.
    st.info(error_title)
    st.write(f"{title}")
    st.write("Missing columns:", missing)
    st.write("Columns present:", list(df.columns))
    st.write("Preview (first row):")
    st.write(df.head(1))


